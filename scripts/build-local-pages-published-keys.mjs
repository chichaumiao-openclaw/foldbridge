#!/usr/bin/env node
// 生成 local-pages 已发布详情页 key 资产（浏览器可 import 的 ES module）。
//
// 扫描 public/rmdb-v3/cases 与 public/rasp-v3/cases 下实际存在的 case 目录，
// 目录名是单次百分号编码的 case key（如 RMDB2PDB%3A10ZT），还原成 family:pdb
// 后合并去重，写出 src/assets/generated/published_case_keys.js。
//
// 这是 localPagesBridgeLinks 运行期 gate 的权威来源——只有真的发布了详情页
// 资产的 case，bridge 才返回 href；其余回退到 SPA 路由，避免点进 404。
// 以实际部署的 public/*/cases 目录为准（不依赖 /Volumes 发布准备根挂载）。
//
// 用法：node scripts/build-local-pages-published-keys.mjs

import { readdir, writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');

const CASE_DIRS = [
  path.join(REPO_ROOT, 'public', 'rmdb-v3', 'cases'),
  path.join(REPO_ROOT, 'public', 'rasp-v3', 'cases')
];

const OUT_PATH = path.resolve(__dirname, '..', 'src', 'assets', 'generated', 'published_case_keys.js');

// 目录名是单次百分号编码（RMDB2PDB%3A10ZT）。decodeURIComponent 还原成 RMDB2PDB:10ZT。
function decodeCaseDirName(name) {
  try {
    return decodeURIComponent(name).trim();
  } catch (_error) {
    return '';
  }
}

async function collectPublishedKeys() {
  const keys = new Set();
  for (const casesDir of CASE_DIRS) {
    if (!existsSync(casesDir)) {
      console.error(`[published-keys] cases dir not found: ${casesDir}`);
      continue;
    }
    const entries = await readdir(casesDir, { withFileTypes: true });
    let dirCount = 0;
    for (const entry of entries) {
      if (!entry.isDirectory()) continue;
      const key = decodeCaseDirName(entry.name);
      if (key.startsWith('RMDB2PDB:') || key.startsWith('RASP2PDB:')) {
        keys.add(key);
        dirCount += 1;
      }
    }
    console.error(`[published-keys] ${String(dirCount).padStart(5)} cases  ${path.relative(REPO_ROOT, casesDir)}`);
  }
  return [...keys].sort();
}

async function main() {
  const keys = await collectPublishedKeys();
  if (!keys.length) {
    console.error('[published-keys] no published case dirs found; refusing to write empty asset');
    process.exit(1);
  }
  const body = keys.map((key) => `  ${JSON.stringify(key)}`).join(',\n');
  const contents = `// AUTO-GENERATED by scripts/build-local-pages-published-keys.mjs — do not edit by hand.\n`
    + `// Source of truth: public/{rmdb-v3,rasp-v3}/cases/ deployed detail-page dirs.\n`
    + `export const LOCAL_PAGES_PUBLISHED_CASE_KEYS = [\n${body}\n];\n`;
  await mkdir(path.dirname(OUT_PATH), { recursive: true });
  await writeFile(OUT_PATH, contents, 'utf8');
  console.error(`[published-keys] wrote ${keys.length} keys -> ${path.relative(REPO_ROOT, OUT_PATH)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
